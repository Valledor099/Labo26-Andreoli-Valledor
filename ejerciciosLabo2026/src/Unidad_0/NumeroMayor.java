package Unidad_0;

public class NumeroMayor {
    public static void main(String[] args) {
        int v1 =3, v2=5;

        if (v1 > v2)
        {
            System.out.println("La variable 1 es mas grande que la segunda");
        }

        else if (v1 == v2)
        {
            System.out.println("Las dos variables son iguales");
        }

        else
        {
            System.out.println("La variable 2 es mas grande que la primera");
        }
    }
}
